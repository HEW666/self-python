# == != > < >= <= 六种运算符
num1 = 10
num2 = 10
print(f"10 == 10的结果是：{num1 == num2}")
# ↑两者内容相同，结果为True

num1 = 15
num2 = 10
print(f"10 != 15的结果是：{num1 != num2}")

name1 = "txwl"
name2 = "txwl1"
print(f"txwl == txwl1的结果是：{name1 == name2}")

# ↓大于小于，大于等于,小于等于
num1 = 10
num2 = 5
print(f"10 > 5结果是：{num1 > num2}")
print(f"10 < 5结果是：{num1 < num2}")

num1 = 11
num2 = 10
print(f"10 >= 10结果是：{num1 >= num2}")
print(f"10 <= 10结果是：{num1 <= num2}")